// GET /api/feed
router.get('/feed', (req, res) => {
  const { filter = 'all' } = req.query;
  let feed = [];

  // Combine timelines and source documents for feed
  const timeline = store.getAll(COLLECTIONS.REGULATORY_TIMELINE).map(t => ({
    id: t.id,
    type: 'regulatory',
    title: t.title,
    summary: t.summary,
    source: t.source_name,
    url: t.source_url,
    date: t.publication_date,
    impact: t.impact_level
  }));
  
  const documents = store.getAll(COLLECTIONS.SOURCE_DOCUMENTS).map(d => ({
    id: d.id,
    type: d.source_type || 'update',
    title: d.title,
    summary: d.summary || d.clean_text?.substring(0, 150) + '...',
    source: d.source_name,
    url: d.source_url || d.pdf_url,
    date: d.publication_date || d.scraped_at,
    impact: 'medium'
  }));

  feed = [...timeline, ...documents].sort((a, b) => new Date(b.date) - new Date(a.date));

  if (filter !== 'all') {
    feed = feed.filter(f => f.type === filter);
  }

  res.json({ success: true, count: feed.length, data: feed });
});

