// GET /api/company/search
router.get('/company/search', (req, res) => {
  const { q } = req.query;
  if (!q) return res.status(400).json({ error: 'Query q is required' });
  
  const allCompanies = store.getAll(COLLECTIONS.COMPANIES);
  const match = matchCompany(q, allCompanies);
  
  if (!match) {
    return res.json({ success: true, match: null, message: "Company not found in tracked database." });
  }
  res.json({ success: true, match });
});

